package com.wgalvao.sdet.java;

/**
 * Variaveis
 * String helloWord = "Hello World!";
 * int integerNumber = 24;
 * double doubleNumber = 2.4;
 * 
 */
public class Java06Variables {
    public static void main(String[] args) {

        String helloWord = "Hello World!";
        System.out.println("String helloWord = " + helloWord);

        int integerNumber = 24;
        System.out.println("int integerNumber = " + integerNumber);

        double doubleNumber = 2.4;
        System.out.println("double doubleNumber = " + doubleNumber);

    }
}
