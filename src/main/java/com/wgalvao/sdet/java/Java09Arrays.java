package com.wgalvao.sdet.java;

/**
 * 
 * Arrays
 *         int[] myIntArray = {1,2,3};
 * 
 */
public class Java09Arrays {
    public static void main(String[] args) {

        int[] myIntArray = {1,2,3};
        System.out.println("myIntArray.length " + myIntArray.length);
        System.out.println("myIntArray.toString() " + myIntArray.toString());
        System.out.println("myIntArray.hashCode() " + myIntArray.hashCode());
        System.out.println("myIntArray[0] " + myIntArray[0]);
        System.out.println("myIntArray[1] " + myIntArray[1]);
        System.out.println("myIntArray[2] " + myIntArray[2]);

    }
}
